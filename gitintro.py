# 1. What is Git?
# Git is a version control system that helps you track changes in your code, collaborate with others, and manage different versions of your project. Key features of Git:

# Tracking changes: Keeps a history of all code changes.

# Branching and merging: Allows developers to work on different features or fixes in isolation (via branches) and later merge them.

# Collaboration: Multiple people can work on the same project without overwriting each other's changes.




# 2. Basic Git Commands
# To use Git, you usually work within a local repository on your computer.

# git init: Initializes a new Git repository in your project folder.



# git status: Shows the current state of your files (untrack, added, modifed etc.).
# git add [file-name]: Stages changes to be committed.


# git commit -m "message": Saves your staged changes with a descriptive message.


